# String -> tudo dentro de aspas
print('Olá, Mundo!')

# String -> tudo dentro de aspas duplas
print("Olá, Mundo!")

# Escape -> para usar aspas dentro de aspas, usamos a barra invertida
print('Olá, Mundo\'!')

# r
print(r'Olá, Mundo\'!')

# String -> aspas duplas dentro de aspas simples
print('Olá, "Mundo"!')