# operadores ariteméticos

# soma
adicao = 10 + 5
print('adicao:', adicao)

# subtração
subtracao = 10 - 5
print('subtracao:', subtracao)

# multiplicação
multiplicacao = 10 * 5
print('multiplicacao:', multiplicacao)

# divisão
divisao = 10 / 5
print('divisao:', divisao)

# divisão inteira
divisao_inteira = 10 // 3
print('divisao_inteira:', divisao_inteira)

# módulo
modulo = 10 % 3
print('modulo:', modulo)

# exponenciação
exponenciacao = 10 ** 2
print('exponenciacao:', exponenciacao)

