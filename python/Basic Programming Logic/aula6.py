# type convertion, typecasting, coercion

print(2 + 2) 
print('2' + '2')
print(int('2') + 2)

print(float('3.14') + 1)

# true and false

print(bool(0)) # false
print(bool(1)) # true
print(bool(-1)) # true
print(bool('')) # false
print(bool(' ')) # true
print(bool([])) # false
print(bool([0])) # true
print(bool(None)) # false

x = 2.2
print(format(x, ".0f"))
